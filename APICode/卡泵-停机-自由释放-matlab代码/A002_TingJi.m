%输入参数
Ma=200;   %总负载扭矩
L=1300;    %抽油杆长度
miu=730;    %粘度
m=0.7;      %制动扭矩倍数
for i=1:10
[t]=ZhiDongShiJian(L,m);
[v]=ZhiDongSuDu(L,m,miu);
[M_re]=NiuJu(m,Ma);
mm(i)=m;           %制动扭矩倍数
Mt(i)=Ma*m;      %制动扭矩
tt(i)=t;        %制动时间
vv(i)=v;        %反转速度
MM_re(i)=M_re;   %残余扭矩
Ma=M_re;         %残余扭矩
miu=miu*m;
if M_re<=10
    break
end
end
tt3=sum(tt);
vv3=vv(1);
disp(['总制动时间为: ', num2str(tt3),' s']);
disp(['最大反转速度为: ', num2str(vv3),' rad/s']);
% 输出表头
fprintf('制动扭矩倍数  制动扭矩(N・m)  反转时间(s)  反转速度(rad/s) 残余扭矩（N・m）\n');

% 输出数据，使用格式化字符串
for i = 1:length(mm)
    fprintf('%12.2d  %-14.2f  %-10.2f  %-12.2f  %-10.2f\n', ...
        mm(i), Mt(i), tt(i), vv(i), MM_re(i));
end

function [t]=ZhiDongShiJian(L,m)
t1=2.44283*exp(-m/0.19801)+0.94096;     %500m
t2=3.62977*exp(-m/0.26325)+1.81065;     %900m
t3=5.28677*exp(-m/0.33864)+2.68065;     %1300m
t4=9.39202*exp(-m/0.35031)+4.23604;     %2000m
t5=16.62899*exp(-m/0.32283)+6.15615;     %3000m
if L<=500
    t=t1;
elseif L<=900
    t=t1+(t2-t1)/(900-500)*(L-500);
elseif L<=1300
    t=t2+(t3-t2)/(1300-900)*(L-900);
elseif L<=2000
    t=t3+(t4-t3)/(2000-1300)*(L-1300);
elseif L<=3000
    t=t4+(t5-t4)/(3000-2000)*(L-2000);
else 
    t=t5;
end
end
